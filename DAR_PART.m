% D_MIDDLE_PART = 2.0D0 * (1.0D0 - coeff_1i_m_C0_m_SQRDZ_d_dt) .* field(2:Nzm1)...
%     - (field(1:Nz - 2) + field(3:Nz));
% D_END_PART = - C2 * (IR + coeff_2_d_3_m_SQRDT * (WNzm1 * field(Nzm1)...
%     + WNz * field(Nz) + WR(IDX(step-1))) * coeff_exp_CR_m_dt);
end